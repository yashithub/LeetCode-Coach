const seen = new Set();
const problems = [];
let orderCounters = {};

for (const [topic, entries] of Object.entries(CATEGORIES)) {
  orderCounters[topic] = 0;
  for (const [id, slug, title, difficulty, objective] of entries) {
    const key = `${id}-${slug}`;
    if (seen.has(key)) continue;
    seen.add(key);
    orderCounters[topic]++;
    const order = orderCounters[topic];
    const minutes = difficulty === 'Easy' ? 15 : difficulty === 'Medium' ? 25 : 40;
    const value = difficulty === 'Easy' ? 6 : difficulty === 'Medium' ? 8 : 9;
    problems.push({
      id, slug, title, difficulty, primaryTopic: topic,
      topics: [topic], prerequisites: [],
      learningObjective: objective,
      progressionOrder: order,
      estimatedMinutes: minutes,
      learningValue: value,
    });
  }
}

// Add cross-topic duplicates as separate entries with different primary topics
for (const [topic, entries] of Object.entries(CATEGORIES)) {
  for (const [id, slug, title, difficulty, objective] of entries) {
    const key = `${id}-${slug}-${topic}`;
    if (problems.some(p => p.id === id && p.primaryTopic === topic)) continue;
    orderCounters[topic] = (orderCounters[topic] || 0) + 1;
    const minutes = difficulty === 'Easy' ? 15 : difficulty === 'Medium' ? 25 : 40;
    const value = difficulty === 'Easy' ? 6 : difficulty === 'Medium' ? 8 : 9;
    problems.push({
      id, slug, title, difficulty, primaryTopic: topic,
      topics: [topic], prerequisites: [],
      learningObjective: objective,
      progressionOrder: orderCounters[topic],
      estimatedMinutes: minutes,
      learningValue: value,
    });
  }
}

problems.sort((a, b) => a.primaryTopic.localeCompare(b.primaryTopic) || a.progressionOrder - b.progressionOrder);

const header = `import type { Problem } from './types';

/** Curated problem bank — ${problems.length} problems across 21 DSA topics */
export const PROBLEMS: Problem[] = `;

const body = JSON.stringify(problems, null, 2)
  .replace(/"([^"]+)":/g, '$1:')
  .replace(/"/g, "'");

writeFileSync(out, header + body + ';\n\nexport const PROBLEM_COUNT = PROBLEMS.length;\n\nexport const PROBLEMS_BY_TOPIC = PROBLEMS.reduce((acc, p) => {\n  if (!acc[p.primaryTopic]) acc[p.primaryTopic] = [];\n  acc[p.primaryTopic].push(p);\n  return acc;\n}, {} as Record<string, Problem[]>);\n\nexport const PROBLEMS_BY_SLUG = Object.fromEntries(PROBLEMS.map(p => [p.slug, p]));\n');

